package loops.forLoop;

public class PrintNumbersAscending {
    public static void main(String[] args){

        /*
        Write a java program to print numbers from 1 to 10 (1 and 10) included

        SOLUTION:
        start point: 1
        end point :10
        update :increment
         */

        for(int numbers = 1; numbers <= 10; numbers++) {
            System.out.println(numbers);



        }




    }
}
